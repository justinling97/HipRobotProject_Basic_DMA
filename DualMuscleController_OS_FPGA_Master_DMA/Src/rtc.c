uint16_t rtc_ms_from_subseconds(uint32_t subSeconds)
{
  // subSeconds: 0..RTC_SYNCH_PREDIV，且为倒计数
  uint32_t ticks = RTC_SYNCH_PREDIV - subSeconds; // 0..RTC_SYNCH_PREDIV
  uint32_t ms = (ticks * 1000U) / (RTC_SYNCH_PREDIV + 1U);
  if (ms > 999U) ms = 999U;
  return (uint16_t)ms;
}

HAL_StatusTypeDef RTC_GetDateTimeMs(rtc_datetime_ms_t* out)
{
  RTC_TimeTypeDef t1 = {0}, t2 = {0};
  RTC_DateTypeDef d  = {0};

  // 读两次时间，确保没跨秒
  if (HAL_RTC_GetTime(&hrtc, &t1, RTC_FORMAT_BIN) != HAL_OK) return HAL_ERROR;
  if (HAL_RTC_GetDate(&hrtc, &d,  RTC_FORMAT_BIN) != HAL_OK) return HAL_ERROR;
  if (HAL_RTC_GetTime(&hrtc, &t2, RTC_FORMAT_BIN) != HAL_OK) return HAL_ERROR;

  if (t2.Seconds != t1.Seconds) {
    // 跨秒了，再来一轮
    if (HAL_RTC_GetTime(&hrtc, &t1, RTC_FORMAT_BIN) != HAL_OK) return HAL_ERROR;
    if (HAL_RTC_GetDate(&hrtc, &d,  RTC_FORMAT_BIN) != HAL_OK) return HAL_ERROR;
  } else {
    t1 = t2;
  }

  out->year  = 2000U + d.Year;
  out->month = d.Month;
  out->day   = d.Date;

  out->hour  = t1.Hours;
  out->min   = t1.Minutes;
  out->sec   = t1.Seconds;
  out->ms    = rtc_ms_from_subseconds(t1.SubSeconds);

  return HAL_OK;
}

